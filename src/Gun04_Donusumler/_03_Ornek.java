package Gun04_Donusumler;

public class _03_Ornek {
    public static void main(String[] args) {
        // 3 farklı (char, int, boolean) tipde değişken tanımlayınız
        // deger atayınız ve ekrana yazdırınız.

        int sinifNo=5;
        char subeAdi='B';
        boolean gectiMi=true;

        System.out.println("sinifNo = " + sinifNo);
        System.out.println("subeAdi = " + subeAdi);
        System.out.println("gectiMi = " + gectiMi);

        System.out.println(sinifNo +"-"+ subeAdi + " "+ gectiMi);
        // aralarina bir kelime eklendiğinde + işareti yan yana birleştirme yaptırır.
        // Direk + koyarsan toplamak isteyecek o yüzden aralarına "" koyduk.


    }

}
