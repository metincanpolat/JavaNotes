package Gun02_Escape_Character;

public class _03_Ornek {
    public static void main(String[] args) {
        System.out.println("Merha Dunya");   //ln var
        System.out.println();  // 1 tane satir acar
        System.out.print("Java ya Hosgeldiniz ");     //ln yok
        System.out.println("Merhaba Java");        //ln var
        // ln yazdiktan sonra alt satira gecer

        // printjava  -> Java derliyor (ara kod a .class) -> JV (exe, isletim sistemine)

        System.out.println("Merhaba");
        System.out.println("Dunya");
        System.out.println("Merhaba \nDunya"); // \n bir alt satira gec

        // PazartesÄ± kelimesinin her harfini ayri bir satira yaziniz

        System.out.println("P\nA\nZ\nA\nR\nT\nE\nS\nI");
    }
}
