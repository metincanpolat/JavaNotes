package Gun14_forLoop;

public class _01_forLoop {
    public static void main(String[] args) {
        // 1 den 5 e kadar olan sayıları ekrana yazdırınız.

        int i=1;
        while (i<=5) {
            System.out.println("i = " + i);
            i++;
        } //1 2 3 4 5

        //for döngüsü

        for (int j=1 ; j<=5; j++)
            System.out.println("j = " + j);

    }
}
