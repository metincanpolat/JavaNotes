package Gun03_Java_Variable;

public class _05_Ornek2 {
    public static void main(String[] args) {
        // Soru: yaricspi verilen bir dairenin cevresini ve alanini bulunuz.

        int r=5;
        double pi=3.14;
        double cevresi=2*pi*r;
        double alani=r*r*pi;

        System.out.println("Alanı = " + alani);
        System.out.println("Çevresi = " + cevresi);

    }
}
