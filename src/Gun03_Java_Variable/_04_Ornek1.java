package Gun03_Java_Variable;

public class _04_Ornek1 {
    public static void main(String[] args) {
       // Soru: iki kenarı tanimli olarak verilen bir dikdörtgenin cevresini ve alanini bulunuz.

        int kisaKenar=5;
        int uzunKenar=7;
        int Cevresi= (kisaKenar+uzunKenar)*2;
        int Alani= kisaKenar*uzunKenar;

        System.out.println("Çevresi = " + Cevresi);
        System.out.println("Alanı = " + Alani);
    }

}
