package Gun08_Operators;

public class _05_JavaAssigmentOperators {
    public static void main(String[] args) {
        int a=10;

        a=a+5; // a nın degerini 5 arttırır
        a+=5;  // a nın artış değeri 5, yukarıdakiyle aynı

        a=a-5; // a nın degerini 5 azaltır.
        a-=5; // a nın azalma değeri 5, yukarıdakiyle aynı

        a=a*5; // a nın degerini 5 ile çarpar.
        a*=5; // a nın çarpan değeri 5, yukarıdakiyle aynı

        a=a/5; // a nın degerini 5 e böler.
        a/=5; // a nın bölen değeri 5, yukarıdakiyle aynı



    }
}
